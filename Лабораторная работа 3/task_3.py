# TODO  Напишите функцию count_letters
def count_letters(text):
    text1 = list(text.lower())
    letters = {}
    for i in text1:
        if i.isalpha() and i not in letters.keys():
            letters[i] = text1.count(i)
    return letters


# TODO Напишите функцию calculate_frequency
def calculate_frequency(letters_amount):
    amount = sum(letters_amount.values())
    for k,v in letters_amount.items():
        letters_amount[k] = v/amount
    return letters_amount


